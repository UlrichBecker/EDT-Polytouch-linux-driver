typedef u16 COORDINATES_T;
#define EDT_INVALID_POINT ((COORDINATES_T)~0)

#define EDT_DIMENSION_TO_COORDINATE( d ) ((d) * 64 - 1)

struct edt_point_t {
	COORDINATES_T x;
	COORDINATES_T y;
};

enum edt_event_t {
	TOUCH_EVENT_DOWN	= 0x00, /* can be received from touch-panel */
	TOUCH_EVENT_UP		= 0x01, /* can be received from touch-panel */
	TOUCH_EVENT_ON		= 0x02, /* can be received from touch-panel */
	TOUCH_EVENT_RESERVED	= 0x03, /* can be received from touch-panel */
	TOUCH_EVENT_NON		= 0xFF  /* will be set by this driver only */
};

