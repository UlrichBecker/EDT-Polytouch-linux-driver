static int edt_ft5x06_ts_reset(struct i2c_client *client,
			struct edt_ft5x06_ts_data *p_tsdata)
{
	int error;

	if (gpio_is_valid(p_tsdata->wake_pin)) {
		error = devm_gpio_request_one(&client->dev,
					p_tsdata->wake_pin, GPIOF_OUT_INIT_LOW,
					"edt-ft5x06 wake");
		if (error) {
			dev_err(&client->dev,
				"Failed to request GPIO %d as wake pin,"
				" error %d\n",
				p_tsdata->wake_pin, error);
			return error;
		}

		msleep(5);
		gpio_set_value(p_tsdata->wake_pin, 1);
	}
	if (gpio_is_valid(p_tsdata->reset_pin)) {
		/* this pulls reset down, enabling the low active reset */
		error = devm_gpio_request_one(&client->dev,
					p_tsdata->reset_pin,
					GPIOF_OUT_INIT_LOW,
					"edt-ft5x06 reset");
		if (error) {
			dev_err(&client->dev,
				"Failed to request GPIO %d as reset pin,"
				" error %d\n",
				p_tsdata->reset_pin, error);
			return error;
		}

		msleep(5);
		gpio_set_value(p_tsdata->reset_pin, 1);
		msleep(300);
	}

	return 0;
}

static int edt_ft5x06_ts_identify(struct i2c_client *p_i2c_client,
					struct edt_ft5x06_ts_data *p_tsdata,
					char *fw_version)
{
	u8 rdbuf[EDT_NAME_LEN];
	char *p;
	int error;
	char *model_name = p_tsdata->name;

	/* see what we find if we assume it is a M06 *
	 * if we get less than EDT_NAME_LEN, we don't want
	 * to have garbage in there
	 */
	memset(rdbuf, 0, sizeof(rdbuf));
	error = edt_ft5x06_ts_readwrite(p_i2c_client, 1, "\xBB",
					EDT_NAME_LEN - 1, rdbuf);
	if (error)
		return error;

	/* probe content for something consistent.
	 * M06 starts with a response byte, M12 gives the data directly.
	 * M09/Generic does not provide model number information.
	 */
	if (!(strncasecmp(rdbuf + 1, "EP0", 3))) {
		p_tsdata->version = EDT_M06;

		/* remove last '$' end marker */
		rdbuf[EDT_NAME_LEN - 1] = '\0';
		if (rdbuf[EDT_NAME_LEN - 2] == '$')
			rdbuf[EDT_NAME_LEN - 2] = '\0';

		/* look for Model/Version separator */
		p = strchr(rdbuf, '*');
		if (p)
			*p++ = '\0';
		strlcpy(model_name, rdbuf + 1, EDT_NAME_LEN);
		strlcpy(fw_version, p ? p : "", EDT_NAME_LEN);
	} else if (!(strncasecmp(rdbuf, "EP0", 3))) {
		p_tsdata->version = EDT_M12;

		/* remove last '$' end marker */
		rdbuf[EDT_NAME_LEN - 2] = '\0';
		if (rdbuf[EDT_NAME_LEN - 3] == '$')
			rdbuf[EDT_NAME_LEN - 3] = '\0';

		/* look for Model/Version separator */
		p = strchr(rdbuf, '*');
		if (p)
			*p++ = '\0';
		strlcpy(model_name, rdbuf, EDT_NAME_LEN);
		strlcpy(fw_version, p ? p : "", EDT_NAME_LEN);
	} else {
		/* if it is not an EDT M06/M12 touchscreen then the model
		 * detection is a bit hairy. The different ft5x06
		 * firmares around don't reliably implement the
		 * identification registers. Well. we'll take a shot.
		 *
		 * the main difference between generic focaltec based
		 * touches and EDT M09 is, that we know how to retrieve
		 * the max coordinates for the latter */
		p_tsdata->version = GENERIC_FT;

		error = edt_ft5x06_ts_readwrite(p_i2c_client, 1, "\xA6",
						2, rdbuf);
		if (error)
			return error;

		strlcpy(fw_version, rdbuf, 2);

		error = edt_ft5x06_ts_readwrite(p_i2c_client, 1, "\xA8",
						1, rdbuf);
		if (error)
			return error;

		/* this "model identification is not exact. Unfortunately
                 * not all firmwares for the ft5x06 put useful values in
                 * the identification registers */
		switch (rdbuf[0]) {
		case 0x35:   /* EDT EP0350M09 */
		case 0x43:   /* EDT EP0430M09 */
		case 0x50:   /* EDT EP0500M09 */
		case 0x57:   /* EDT EP0570M09 */
		case 0x70:   /* EDT EP0700M09 */
			p_tsdata->version = EDT_M09;
			snprintf(model_name, EDT_NAME_LEN, "EP0%i%i0M09",
				rdbuf[0] >> 4, rdbuf[0] & 0x0F);
			break;
		case 0xa1:   /* EDT EP1010ML00 */
			p_tsdata->version = EDT_M09;
			snprintf(model_name, EDT_NAME_LEN, "EP%i%i0ML00",
				rdbuf[0] >> 4, rdbuf[0] & 0x0F);
			break;
		case 0x5a:   /* Solomon Goldentek Display */
			snprintf(model_name, EDT_NAME_LEN, "GKTW50SCED1R0");
			break;
		default:
			snprintf(model_name, EDT_NAME_LEN,
                                 "generic ft5x06 (%02x)",
				rdbuf[0]);
			break;
		}
	}

	return 0;
}

#define EDT_ATTR_CHECKSET(name, reg) \
do {								\
	if (pdata->name >= edt_ft5x06_attr_##name.limit_low &&		\
	    pdata->name <= edt_ft5x06_attr_##name.limit_high)		\
		edt_ft5x06_register_write(p_tsdata, reg, pdata->name);	\
} while (0)

#define EDT_GET_PROP(name, reg) {				\
	u32 val;						\
	if (of_property_read_u32(np, #name, &val) == 0)		\
		edt_ft5x06_register_write(p_tsdata, reg, val);	\
}

static void edt_ft5x06_ts_get_dt_defaults(struct device_node *np,
					struct edt_ft5x06_ts_data *p_tsdata)
{
	struct edt_reg_addr *reg_addr = &p_tsdata->reg_addr;

	EDT_GET_PROP(threshold, reg_addr->reg_threshold);
	EDT_GET_PROP(gain, reg_addr->reg_gain);
	EDT_GET_PROP(offset, reg_addr->reg_offset);
}

static void
edt_ft5x06_ts_get_defaults(struct edt_ft5x06_ts_data *p_tsdata,
			   const struct edt_ft5x06_platform_data *pdata)
{
	struct edt_reg_addr *reg_addr = &p_tsdata->reg_addr;

	if (!pdata->use_parameters)
		return;

	/* pick up defaults from the platform data */
	EDT_ATTR_CHECKSET(threshold, reg_addr->reg_threshold);
	EDT_ATTR_CHECKSET(gain, reg_addr->reg_gain);
	EDT_ATTR_CHECKSET(offset, reg_addr->reg_offset);
	if (reg_addr->reg_report_rate != NO_REGISTER)
		EDT_ATTR_CHECKSET(report_rate, reg_addr->reg_report_rate);
}

static void
edt_ft5x06_ts_get_parameters(struct edt_ft5x06_ts_data *p_tsdata)
{
	int reg_value;
	struct edt_reg_addr *p_reg_addr = &p_tsdata->reg_addr;

	p_tsdata->threshold = edt_ft5x06_register_read(p_tsdata,
						p_reg_addr->reg_threshold);
	p_tsdata->gain = edt_ft5x06_register_read(p_tsdata,
						  p_reg_addr->reg_gain);
	p_tsdata->offset = edt_ft5x06_register_read(p_tsdata,
						    p_reg_addr->reg_offset);
	if (p_reg_addr->reg_report_rate != NO_REGISTER)
		p_tsdata->report_rate = edt_ft5x06_register_read(p_tsdata,
						p_reg_addr->reg_report_rate);

	p_tsdata->dimension.x = EDT_INVALID_POINT;
	p_tsdata->dimension.y = EDT_INVALID_POINT;

	if (p_tsdata->version == EDT_M06 ||
	    p_tsdata->version == EDT_M09 ||
	    p_tsdata->version == EDT_M12) {

		reg_value = edt_ft5x06_register_read(p_tsdata,
						 p_reg_addr->reg_num_x);
		if( reg_value > 0 )
			p_tsdata->dimension.x =
				EDT_DIMENSION_TO_COORDINATE(reg_value);

		reg_value = edt_ft5x06_register_read(p_tsdata,
						 p_reg_addr->reg_num_y);
		if( reg_value > 0 )
			p_tsdata->dimension.y =
				EDT_DIMENSION_TO_COORDINATE(reg_value);
	}
}

static void
edt_ft5x06_ts_set_regs(struct edt_ft5x06_ts_data *p_tsdata)
{
	struct edt_reg_addr *p_reg_addr = &p_tsdata->reg_addr;

	switch (p_tsdata->version) {
	case EDT_M06:
		p_reg_addr->reg_threshold = WORK_REGISTER_THRESHOLD;
		p_reg_addr->reg_report_rate = WORK_REGISTER_REPORT_RATE;
		p_reg_addr->reg_gain = WORK_REGISTER_GAIN;
		p_reg_addr->reg_offset = WORK_REGISTER_OFFSET;
		p_reg_addr->reg_num_x = WORK_REGISTER_NUM_X;
		p_reg_addr->reg_num_y = WORK_REGISTER_NUM_Y;
		break;

	case EDT_M09:
	case EDT_M12:
		p_reg_addr->reg_threshold = M09_REGISTER_THRESHOLD;
		p_reg_addr->reg_gain = M09_REGISTER_GAIN;
		p_reg_addr->reg_offset = M09_REGISTER_OFFSET;
		p_reg_addr->reg_num_x = M09_REGISTER_NUM_X;
		p_reg_addr->reg_num_y = M09_REGISTER_NUM_Y;
		break;

	case GENERIC_FT:
		/* this is guesswork */
		p_reg_addr->reg_threshold = M09_REGISTER_THRESHOLD;
		p_reg_addr->reg_gain = M09_REGISTER_GAIN;
		p_reg_addr->reg_offset = M09_REGISTER_OFFSET;
		break;
	}
}

