/*
 * Minimum number of incoming interrupts after a first new
 * finger-contact, before the touch-event will forwarded to the
 * operating system.
 * This step could be necessary to cancel electrical noise interfering.
 * NOTE:
 * - When this value is set to zero no noise canceling will be done!
 * - The higher this value the more sluggish will be the touch-event!
 */
#define EDT_DEFAULT_MIN_RECEIVE_COUNT	1

/*
 * Touch-event-interrupt timeout in milliseconds.
 * This is a safety timeout for the case a touch-up (release) interrupt
 * is missing and makes sure that the OS becomes in any cases
 * a touch-up-event in this case generated by the timer-function.
 */
#define EDT_DEFAULT_TUOCH_TIMEOUT	100 /* msec */

