static int edt_ft5x06_factory_mode(struct edt_ft5x06_ts_data *p_tsdata)
{
	struct i2c_client *p_i2c_client = p_tsdata->p_i2c_client;
	int retries = EDT_SWITCH_MODE_RETRIES;
	int ret;
	int error;

	disable_irq(p_i2c_client->irq);

	if (!p_tsdata->raw_buffer) {
		if (coordinates_invalid( &p_tsdata->dimension )) {
			error = -EINVAL;
			goto err_out;
		}
			
		p_tsdata->raw_bufsize = 
			COORDINATE_TO_EDT_DIMENSION(p_tsdata->dimension.x) *
			COORDINATE_TO_EDT_DIMENSION(p_tsdata->dimension.y) *
			sizeof(COORDINATES_T);
		p_tsdata->raw_buffer = kzalloc(p_tsdata->raw_bufsize,
					       GFP_KERNEL);
		if (!p_tsdata->raw_buffer) {
			error = -ENOMEM;
			goto err_out;
		}
	}

	/* mode register is 0x3c when in the work mode */
	if (p_tsdata->version != EDT_M06)
		goto m09_out;

	error = edt_ft5x06_register_write(p_tsdata, WORK_REGISTER_OPMODE, 0x03);
	if (error) {
		dev_err(&p_i2c_client->dev,
			"failed to switch to factory mode, error %d\n", error);
		goto err_out;
	}

	p_tsdata->factory_mode = true;
	do {
		mdelay(EDT_SWITCH_MODE_DELAY);
		/* mode register is 0x01 when in factory mode */
		ret = edt_ft5x06_register_read(p_tsdata, 
					       FACTORY_REGISTER_OPMODE);
		if (ret == 0x03)
			break;
	} while (--retries > 0);

	if (retries == 0) {
		dev_err(&p_i2c_client->dev, 
			"not in factory mode after %dms.\n",
			EDT_SWITCH_MODE_RETRIES * EDT_SWITCH_MODE_DELAY);
		error = -EIO;
		goto err_out;
	}

	return 0;

err_out:
	kfree(p_tsdata->raw_buffer);
	p_tsdata->raw_buffer = NULL;
	p_tsdata->factory_mode = false;
	enable_irq(p_i2c_client->irq);

	return error;

m09_out:
	dev_err(&p_i2c_client->dev,
		"No factory mode support for M09/M12/GENERIC_FT\n");
	return -EINVAL;

}

static int edt_ft5x06_work_mode(struct edt_ft5x06_ts_data *p_tsdata)
{
	struct i2c_client *p_i2c_client = p_tsdata->p_i2c_client;
	int retries = EDT_SWITCH_MODE_RETRIES;
	struct edt_reg_addr *reg_addr = &p_tsdata->reg_addr;
	int ret;
	int error;

	/* mode register is 0x01 when in the factory mode */
	error = edt_ft5x06_register_write(p_tsdata,
					  FACTORY_REGISTER_OPMODE, 0x1);
	if (error) {
		dev_err(&p_i2c_client->dev,
			"failed to switch to work mode, error: %d\n",
			error);
		return error;
	}

	p_tsdata->factory_mode = false;

	do {
		mdelay(EDT_SWITCH_MODE_DELAY);
		/* mode register is 0x01 when in factory mode */
		ret = edt_ft5x06_register_read(p_tsdata, WORK_REGISTER_OPMODE);
		if (ret == 0x01)
			break;
	} while (--retries > 0);

	if (retries == 0) {
		dev_err(&p_i2c_client->dev, "not in work mode after %dms.\n",
			EDT_SWITCH_MODE_RETRIES * EDT_SWITCH_MODE_DELAY);
		p_tsdata->factory_mode = true;
		return -EIO;
	}

	kfree(p_tsdata->raw_buffer);
	p_tsdata->raw_buffer = NULL;

	/* restore parameters */
	edt_ft5x06_register_write(p_tsdata, reg_addr->reg_threshold,
				  p_tsdata->threshold);
	edt_ft5x06_register_write(p_tsdata, reg_addr->reg_gain,
				  p_tsdata->gain);
	edt_ft5x06_register_write(p_tsdata, reg_addr->reg_offset,
				  p_tsdata->offset);
	if (reg_addr->reg_report_rate)
		edt_ft5x06_register_write(p_tsdata, reg_addr->reg_report_rate,
				  p_tsdata->report_rate);

	enable_irq(p_i2c_client->irq);

	return 0;
}

static int edt_ft5x06_debugfs_mode_get(void *data, u64 *mode)
{
	struct edt_ft5x06_ts_data *p_tsdata = data;

	*mode = p_tsdata->factory_mode;

	return 0;
};

static int edt_ft5x06_debugfs_mode_set(void *data, u64 mode)
{
	struct edt_ft5x06_ts_data *p_tsdata = data;
	int retval = 0;

	if (mode > 1)
		return -ERANGE;

	spin_lock_irq( &p_tsdata->spinlock );

	if (mode != p_tsdata->factory_mode) {
		retval = mode ? edt_ft5x06_factory_mode(p_tsdata) :
				edt_ft5x06_work_mode(p_tsdata);
	}

	spin_unlock_irq( &p_tsdata->spinlock );

	return retval;
};

